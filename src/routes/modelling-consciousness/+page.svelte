	<div><div grid-col="8" grid-pad="2" class=""><small>upon further research of the unmoved, I discovered a vast, unseen world that is beyond senses. the unmoving is one, it is eternal and is the source of all that is: itself, it illumines the five senses, and through these five senses its light reaches the phenomenal world, and thus creation is experienced. this unmoving element of reality, which is infact the only thing that is real in the truest sense, has slowly made its way into modern psychology. it can be termed consciousness. david chalmers**&nbsp; put forward this idea for psychologists to ponder in his paper <i>facing up to the problem of consciousness. </i><br>
	
	<br>how can I inspire in a viewer the idea of something that exists in the non-phenomal world? thus my process brought me full circle: to use a comparison of micro-macro footage in order to explain this all-encompassing model of consciousness. by showing to the viewer all that is phenomenally; all that moves and manifests, I hope to inspire him into knowing that which does not move and is not manifest externally. <br>
	
