	<div><small><b>the moving and the unmoved</b></small><h2><br></h2><small>
	the phenomenal world that we experience follows an unwritten law of constant movement - everything that we perceive is always in motion, even though some of these things may at first appear to be at rest. we have been taught that a tree, which stands on solid ground with centred trunk and exploding leaf-structures does not move. but in itself, the tree is a hive of activity with visible growth and decay resulting from movement of the elements that make up the tree: radially outwardly extending trunk, flowing tree sap, roots that grow across the forest floor, and so on. thus it moves. even an inanimate object, such as a pen on a tabletop is not stationary: it moved before someone put it there and will move due to another cause. everything moves: things that do not seem to move in themselves, such as soil, or lifeless objects are also subject to movement by law of nature - the nonstop process of growth and decay touches everything in this world.<br>
	<br>then what is it that does not move? all entities in existence share a bipolar relation with an opposite - white has black, object has subject, the seen has the unseen: thus the unmoving must also exist. however this entity of non-movement stands distinct from the phenomenal world - it is of the noumenal. the isavsya upanishad in verses 4 and 5 explains the nature of supreme reality in terms of movement and non-movement:<br>
	<br>
	<i>‘the self is one. it is unmoving: yet faster than the mind. us moving faster, It is beyond the reach of the senses. ever steady, it outstrips all that run. by its mere presence, the cosmic energy is enabled to sustain the activities of living beings.’ </i><br>
	<br>
	<i>‘it moves; it moves not. it is far: it is very near. it is inside all this: it is verily outside all this.’ </i><br>
	<br>it can be said that the non-moving is the source of the moving. they correspond respectively to eternal immutability and unceasing change, as radhakrishnan* puts it. for more on this, watch the following clips:<br>
	
	
