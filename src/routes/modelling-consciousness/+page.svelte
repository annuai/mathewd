		<h2>modeling consciousness</h2><br>
	<small>when I was very young, I had often wished that I had a microscope. this wish went unfulfilled for some reason. in september 2018, the bio lab at design academy eindhoven where I am currently doing my masters, got a microscope with a high definition camera attached to it. once I got my hands on it, a hundred ideas that had been lying buried in my subconscious started to coil up and reveal themselves in interesting forms.<br>
	<br>
	 I played with the microscope, looking at everything I could find: body fluids, food and soil are a few of these things. as these long evenings in the bio lab with my eyes glued to micro-world magnifications progressed, one idea stood apart from the many ideas that were rising from the dust of the mind: this was the idea that putting microscopic images of life along with images from larger scales such as human and planetary side by side: a micro-macro comparison, could be a way to explain complex metaphysical concepts that otherwise require long periods of study. I made the film <i>before my beginning</i> as a first experiment.<br>
	
